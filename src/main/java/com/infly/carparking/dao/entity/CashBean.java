package com.infly.carparking.dao.entity;

public class CashBean {
	private int id;
	private String name;
	private float num;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getNum() {
		return num;
	}

	public void setNum(float num) {
		this.num = num;
	}
}
