package com.infly.carparking.dao.entity;

public class UserInfoBean {
	private int id;
	private String userInfoName;
	private String userInfoPassword;
	private String userInfoSex;
	private String userInfoTel;
	private String userInfoAddress;
	private String userInfoCarno;
	private int userInfoNo;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUserInfoName() {
		return userInfoName;
	}

	public void setUserInfoName(String userInfoName) {
		this.userInfoName = userInfoName;
	}

	public String getUserInfoPassword() {
		return userInfoPassword;
	}

	public void setUserInfoPassword(String userInfoPassword) {
		this.userInfoPassword = userInfoPassword;
	}

	public String getUserInfoSex() {
		return userInfoSex;
	}

	public void setUserInfoSex(String userInfoSex) {
		this.userInfoSex = userInfoSex;
	}

	public String getUserInfoTel() {
		return userInfoTel;
	}

	public void setUserInfoTel(String userInfoTel) {
		this.userInfoTel = userInfoTel;
	}

	public String getUserInfoAddress() {
		return userInfoAddress;
	}

	public void setUserInfoAddress(String userInfoAddress) {
		this.userInfoAddress = userInfoAddress;
	}

	public String getUserInfoCarno() {
		return userInfoCarno;
	}

	public void setUserInfoCarno(String userInfoCarno) {
		this.userInfoCarno = userInfoCarno;
	}

	public int getUserInfoNo() {
		return userInfoNo;
	}

	public void setUserInfoNo(int userInfoNo) {
		this.userInfoNo = userInfoNo;
	}

}